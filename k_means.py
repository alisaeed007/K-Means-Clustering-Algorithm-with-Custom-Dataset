from sklearn.cluster import KMeans
import pandas as pd
from matplotlib import pyplot as plot

from google.colab import drive
drive.mount('/content/drive')
path = '/content/drive/MyDrive/abc/Kmeans.csv'
dataset = pd.read_csv(path,skipinitialspace=True)

print(dataset)

plot.scatter(dataset.Height,dataset.Weight)
algo = KMeans(n_clusters=2)
y_predicted = algo.fit_predict(dataset[['Height','Weight']])
dataset['cluster']=y_predicted

algo.cluster_centers_
dataset1 = dataset[dataset.cluster==0]
dataset2 = dataset[dataset.cluster==1]


plot.scatter(dataset1['Height'],dataset1['Weight'],color='green',label='Height')
plot.scatter(dataset2['Height'],dataset2['Weight'],color='red',label='Weight')

plot.scatter(algo.cluster_centers_[:,0],algo.cluster_centers_[:,1],color='black',marker='*',label='centroid')
plot.legend()